<script>
const monthYear = document.getElementById('month-year');
const calendar = document.getElementById('calendar');
const selectedBox = document.getElementById('selectedDateBox');
const selectedText = document.getElementById('selectedDateText');
const hiddenInput = document.getElementById('hiddenSelectedDate');
const prevBtn = document.getElementById('prev');
const nextBtn = document.getElementById('next');

let currentDate = new Date(); // Start from current month/year

function renderCalendar(date) {
    const year = date.getFullYear();
    const month = date.getMonth();

    const firstDay = new Date(year, month, 1).getDay();
    const lastDate = new Date(year, month + 1, 0).getDate();

    const monthNames = [
        'January','February','March','April','May','June',
        'July','August','September','October','November','December'
    ];

    monthYear.textContent = `${monthNames[month]} ${year}`;

    let days = '';

    // Weekday labels
    const dayLabels = ['Su','Mo','Tu','We','Th','Fr','Sa'];
    dayLabels.forEach(label => {
        days += `<div class="day-label">${label}</div>`;
    });

    // Empty cells before start of month
    for (let i = 0; i < firstDay; i++) {
        days += `<div class="day-cell inactive"></div>`;
    }

    // Actual days
    for (let day = 1; day <= lastDate; day++) {
        days += `<div class="day-cell">${day}</div>`;
    }

    calendar.innerHTML = days;

    // Add click functionality for days
    document.querySelectorAll('.day-cell:not(.inactive)').forEach(cell => {
        cell.addEventListener('click', () => {
            document.querySelectorAll('.day-cell').forEach(c => c.classList.remove('selected'));
            cell.classList.add('selected');
            const fullDate = `${monthNames[month]} ${cell.textContent}, ${year}`;
            selectedText.textContent = fullDate;
            hiddenInput.value = fullDate;
            selectedBox.style.display = 'block';
        });
    });
}

prevBtn.addEventListener('click', () => {
    currentDate.setMonth(currentDate.getMonth() - 1);
    renderCalendar(currentDate);
});

nextBtn.addEventListener('click', () => {
    currentDate.setMonth(currentDate.getMonth() + 1);
    renderCalendar(currentDate);
});

// Initialize calendar
renderCalendar(currentDate);
